  , "`Shift+arrows` grow a box -- and copy/paste understands shapes:"
  , "copying a box and pasting over a single cell spreads it out, while"
  , "pasting one value over a box fills it. The table has its own undo,"
  , "and the file is only rewritten when you save."
  , ""
  , "## Viewing images"
  , ""
  , "Opening a PNG, JPEG, GIF, WebP, BMP or Netpbm file shows the image"
  , "right in the terminal, scaled to fit in 24-bit colour half-blocks"
  , "(press `a` for an ASCII-art rendering instead). Files are detected"
  , "by content, not extension. Drag a rectangle with the mouse to zoom"
  , "into that region; a click or `Esc` zooms back out. The view is"
  , "read-only -- everything else about the editor keeps working."
  , ""
  , "## Configuration"
  , ""
  , "The friendly way: File > Settings (`Ctrl+,`) opens a settings page"
  , "with every option on it. Move with the arrows; `Left`/`Right` (or a"
