  , "applies immediately so you can see it behind the dialog. `Save`"
  , "writes your choices to the config file for future sessions --"
  , "comments and unfamiliar lines in the file are left alone -- and"
  , "`Cancel` (or `Esc`) puts everything back the way it was."
  , ""
  , "The same defaults can be edited by hand in"
  , "`~/.config/cmedit/config` (XDG_CONFIG_HOME is honoured). One"
